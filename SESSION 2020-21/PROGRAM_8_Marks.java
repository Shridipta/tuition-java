public class PROGRAM_8_Marks
{
    public static void main (int marks)
    {
        if (marks>=80)
            System.out.println("Grade A");
        else if (marks>=60)
            System.out.println("Grade B");
        else if (marks>=40)
            System.out.println("Grade C");
        else
            System.out.println("Grade D");
    }
}